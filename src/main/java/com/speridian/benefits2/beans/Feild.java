package com.speridian.benefits2.beans;

public class Feild {
	private String feildvalue;
	private String feildid;
	private String category;
	private String feildtype;

	public String getCategory() {
		return category;
	}

	public void setCategory(String category) {
		this.category = category;
	}

	public String getFeildtype() {
		return feildtype;
	}

	public void setFeildtype(String feildtype) {
		this.feildtype = feildtype;
	}

	public String getFeildvalue() {
		return feildvalue;
	}

	public void setFeildvalue(String feildvalue) {
		this.feildvalue = feildvalue;
	}

	public String getFeildid() {
		return feildid;
	}

	public void setFeildid(String feildid) {
		this.feildid = feildid;
	}

	
	

}
